package org.character;

public class IndexedCharacter {
public static void main(String[] args) {
	String s1="GreensTechnology";
	String s2="SeleniumAutomationtool";
	String s3="velmurugan";
	String s4="j a v a p r o g r a m";
	String s5="9095484678";
int a = s1.lastIndexOf('o');
System.out.println(a);
int b = s2.indexOf('o');
System.out.println(b);
int c = s3.indexOf('n');
System.out.println(c);
int d = s4.lastIndexOf('4');
System.out.println(d);
int e = s5.indexOf('8');
System.out.println(e);

}
}
